// 読みの表示整形（KANJIDIC2 → 子ども向け）
// 訓: 送り仮名の区切り「.」を除き、接辞形「-〜」は落とす。多くても3つ
// 音: カタカナのまま。多くても2つ
export function formatReadings(readings) {
  const kun = readings.kun
    .filter(r => !r.startsWith('-') && !r.endsWith('-'))
    .map(r => r.replace(/\./g, ''))
    .slice(0, 3);
  const on = readings.on.slice(0, 2);
  return { kun, on };
}
