# 語の切れ目とみなす直前文字（助詞・読点等＋形容詞語尾）。これ以外が直前なら語中の可能性
BOUNDARY_CHARS = set('をにでとがはのへもや、。ないて')
# 連濁しうる清音の頭（か・さ・た・は行）
VOICEABLE_HEAD = set('かきくけこさしすせそたちつてとはひふへほ')
# 促音化しやすい末尾と後続
SOKUON_TAIL = set('くちつ')
SOKUON_NEXT = set('かきくけこさしすせそたちつてとはひふへほぱぴぷぺぽ')

KANJI_RE = re.compile(r'[一-鿿]')


def main():
    kanji = json.load(open(REPO / 'data' / 'kanji-g1.json', encoding='utf-8'))
    order = [k['char'] for k in kanji['kanji']]
    readings_of = {k['char']: set(k['readings']['on']) | set(k['readings']['kun'])
                   for k in kanji['kanji']}

    errors, warnings = [], []

    # ---- チェックA: 選定読み ----
    if set(SELECTED) != set(order):
        errors.append('選定エントリが80字と一致しない')
    for char, sel in SELECTED.items():
        for r in sel['on'] + sel['kun']:
            if r not in readings_of.get(char, set()):
                errors.append(f'{char}: 選定読み「{r}」が kanji-g1.json にない')

    # ---- チェックB/C: 文 ----
    if set(SENTENCES) != set(order):
        errors.append('文テーブルの字が80字と一致しない')
    records = []
    used_readings = {c: set() for c in order}
    for ci, char in enumerate(order, 1):
        rows = SENTENCES.get(char, [])
        if not (2 <= len(rows) <= 3):
            errors.append(f'{char}: 文が{len(rows)}件（2〜3件が規定）')
        sel = set(SELECTED[char]['on']) | set(SELECTED[char]['kun'])
        has_yomi = has_kaki = False
        for si, (text, reading, uses) in enumerate(rows, 1):
            rid = f'g1-{ci:03d}-{si}'
            uses = uses or ['yomi', 'kaki']
            kanjis = KANJI_RE.findall(text)
            if kanjis != [char]:
                errors.append(f'{rid}: 文中の漢字が {kanjis}（{char} 1回のみが規定）')
                continue
            if not (8 <= len(text) <= 18):
                errors.append(f'{rid}: 文長{len(text)}（8〜18が規定）: {text}')
            start = text.index(char)
            if reading not in sel:
                errors.append(f'{rid}: 読み「{reading}」が選定読みにない')
            used_readings[char].add(reading)
            if '.' in reading:
                okuri = reading.split('.', 1)[1]
                after = text[start + 1:]
                if not after.startswith(okuri):
                    warnings.append(f'{rid}: 送り仮名「{okuri}」と直後「{after[:3]}…」が不一致（活用形なら可）: {text}')
            # ---- Phase 3d 警告装置: 連濁・促音化の疑い（機械は疑うだけ。判定は人）----
            def hira(ch):
                return chr(ord(ch) - 0x60) if 'ァ' <= ch <= 'ヶ' else ch
            if rid not in RENDAKU_REVIEWED:
                head = reading.lstrip('-').split('.')[0]
                head_kana = hira(head[0]) if head else ''
                if start > 0 and text[start - 1] not in BOUNDARY_CHARS and head_kana in VOICEABLE_HEAD:
                    warnings.append(f'{rid}: 連濁疑い（語中の清音読み「{reading}」）: {text} → 人手確認のうえ RENDAKU_REVIEWED へ')
                disp = reading.replace('.', '').replace('-', '')
                last_kana = hira(disp[-1]) if disp else ''
                nxt = text[start + 1] if start + 1 < len(text) else ''
                if '.' not in reading and last_kana in SOKUON_TAIL and nxt in SOKUON_NEXT:
                    warnings.append(f'{rid}: 促音化疑い（「{reading}」の直後が{nxt}）: {text} → 人手確認のうえ RENDAKU_REVIEWED へ')
            if 'yomi' in uses:
                has_yomi = True
            if 'kaki' in uses:
                has_kaki = True
            records.append({
                'id': rid,
                'text': text,
                'target': {'char': char, 'reading': reading, 'start': start, 'len': 1},
                'use': uses,
            })
        if not has_yomi:
            errors.append(f'{char}: yomi用の文がない')
        if not has_kaki:
            errors.append(f'{char}: kaki用の文がない')

    for char in order:
        unused = (set(SELECTED[char]['on']) | set(SELECTED[char]['kun'])) - used_readings[char]
        if unused:
            warnings.append(f'{char}: 選定読み {sorted(unused)} を使う文がない')

    if warnings:
        print(f'警告（照読で確認・エラーではない）: {len(warnings)}件', file=sys.stderr)
        for w in warnings:
            print('  ⚠', w, file=sys.stderr)
    if errors:
        print(f'機械チェック失敗（{len(errors)}件）:', file=sys.stderr)
        for e in errors:
            print('  -', e, file=sys.stderr)
        sys.exit(1)

    out1 = {
        'schemaVersion': 1,
        'grade': 1,
        'kenLevel': 10,
        'sources': {
            'kanjidic2': '読みの表記は KANJIDIC2 (c) EDRDG, CC BY-SA 4.0 の原表記に準拠',
            'policy': '小1の生活で普通に使う読みのみ選定（迷ったら入れない）。Phase 3a・照読検収',
        },
        'readings': {c: SELECTED[c] for c in order},
    }
    out2 = {
        'schemaVersion': 1,
        'grade': 1,
        'kenLevel': 10,
        'sources': {
            'origin': '全文自前生成（既存の問題集・ドリル・教材は参照していない）。Phase 3a・照読検収',
        },
        'sentences': records,
    }
    (REPO / 'data' / 'exam-readings-g1.json').write_text(
        json.dumps(out1, ensure_ascii=False, indent=1), encoding='utf-8')
    (REPO / 'data' / 'sentences-g1.json').write_text(
        json.dumps(out2, ensure_ascii=False, indent=1), encoding='utf-8')
    (REPO / 'data' / 'rendaku-audit-g1.json').write_text(
        json.dumps({'schemaVersion': 1, **AUDIT}, ensure_ascii=False, indent=1), encoding='utf-8')
    n_r = sum(len(v['on']) + len(v['kun']) for v in SELECTED.values())
    print(f'OK: 出題読み{n_r}件・文{len(records)}件を出力（機械チェック全通過・警告{len(warnings)}件）')


if __name__ == '__main__':
    main()
